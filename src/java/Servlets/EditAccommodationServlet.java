    // <editor-fold defaultstate="collapsed" desc="HttpServlet methods. Click on the + sign on the left to edit the code.">
    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {

        Filter.getPage(request, response, "EditAccommodation").forward(request, response);
    }

    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        int id = Integer.parseInt(request.getParameter("id"));
        int typename = Integer.parseInt(request.getParameter("type"));
        String location = request.getParameter("location");
        String description = request.getParameter("description");
        int charge = Integer.parseInt(request.getParameter("charge"));
        DataAccessImplementation room = new DataAccessImplementation();
        RoomModel roomModel = new RoomModel(id, typename, true, location, charge, description);
        if (room.insertEditRoom(roomModel)) {
            response.sendRedirect("admin");
        } else {
            response.sendRedirect("EditAccommodation");
        }
    }

